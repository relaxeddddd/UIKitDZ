import UIKit

/// Расшерение для выбора шрифта и его размера
public extension UIFont {
    static var verdana18: UIFont {
        UIFont(name: "Verdana-Bold", size: 18)!
    }

    static var verdana14: UIFont {
        UIFont(name: "Verdana-Bold", size: 14)!
    }
}
